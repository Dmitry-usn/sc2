                Музейный центр «Площадь Мира» (разговорные наименования — «музей на Стрелке», «КИЦ») — 
                один из наиболее известных музеев Красноярска, крупнейшая в Сибири 
                выставочная площадка современного искусства (выставочная площадь — 2 500 
                квадратных метров, площадь временных выставок — 1 037 квадратных метров, 
                единиц хранения — 15 725, из них 14 226 предметов основного фонда, среднее 
                количество посетителей в год — четыреста тысяч). Музейный центр был создан 17 
                апреля 1987 года как Красноярский филиал Центрального музея В. И. Ленина. Он 
                оказался тринадцатым по счёту, единственным на всю Сибирь и Дальний Восток, и 
                последним из созданных в СССР.Здание музея спроектировано под руководством 
                заслуженного архитектора России, член-корреспондента Российской академии художеств 
                и Российской академии архитектуры и строительных наук Арэга Саркисовича Демирханова 
                и построено в стиле советского модернизма. В 1991 году название музея сменилось 
                на Красноярский культурно-исторический музейный центр. А по профилю, в отличие 
                от обычных музеев, он обратился не к прошлому, а к будущему культуры и к видению 
                настоящего. Новая концепция музейного центра была разработана Российским институтом 
                культурологии, Министерством культуры РФ и Российской академией наук совместно с 
                коллективом Красноярского культурно-исторического центра в 1993 году и предполагала 
                создание на основе филиала музея В. И. Ленина г. Красноярска многофункционального 
                культурно–исторического музейного комплекса, как «инновационного проекта, 
                рассчитанного на эксперимент и поиск, на разработку принципиально новых форм 
                деятельности, а не на воспроизведение какого–либо из существующих типов учреждений 
                культуры». Главным ядром концепции стало развитие музея через проекты. 
                Новая концепция позволила не закрывать музей ни на один день. Музейный центр 
                оставался по-прежнему хранителем культурных ценностей и в то же время старался 
                представить новые пространства и смыслы. С этого времени музей стремится 
                создавать новые проекты, многие из которых оказались осуществлёнными впервые 
                в музейной жизни России. Впоследствии музей был переименован в Красноярский 
                музейный центр. В 2017 году музей был переименован в «Музейный центр “Площадь Мира”».
                 Это привязка как к географическому расположению и адресу — площадь Мира, 1, 
                 так и к философии и миссии музея — открывать современное искусство со всего 
                 мира в центре Сибири, стать пространством для изучения и представления 
                 актуальных произведений, отправной точкой для будущих авторов.
