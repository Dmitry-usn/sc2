                Музей-усадьба В. И. Сурикова — музей, расположенный в доме Василия Ивановича Сурикова.
                Музей создан в 1948 году в честь 100-летия со дня рождения художника. 
                Музей расположен на территории усадьбы Суриковых площадью 0,25 гектара в центре 
                Красноярска (Ленина, 98). Выставочная площадь — 357 м², 1300 единиц хранения, 
                из них 92 работы В. И. Сурикова. Дом построен в 1830-е годы из лиственницы. 
                Усадьба была типичной для сибирской городской застройки XIX века. На территории 
                усадьбы, обнесённой глухим заплотом, располагается двухэтажный дом с крыльцом со 
                двора, баня по-чёрному, завозня (амбар и конюшня под одной крышей), флигель, 
                построенный братом художника в 1900-е годы. Второй этаж дома сдавался в аренду. 
                Дом незначительно перестраивался в 1895 году. В этом доме 12 января 
                (по старому стилю) 1848 года родился Василий Иванович Суриков. В 1854 году отца 
                Василия Ивановича перевели на службу в село Сухой Бузим (ныне Сухобузимское) и 
                семья выехала из Красноярска. После смерти отца — Ивана Васильевича — семья 
                вернулась в Красноярск в 1859 году. 11 декабря 1868 года Василий Иванович уехал 
                из Красноярска в Санкт-Петербург. Впервые создать музей В. И. Сурикова 
                предлагалось в 1920-е годы. Но в то время в доме жил младший брат художника — 
                Александр Иванович. В бывшей завозне работала рисовальная школа. 
                В 1926 году на доме была установлена мемориальная доска. Александр Иванович 
                Суриков умер в 1930 году. Часть мебели и некоторые вещи из дома Суриковых были 
                переданы в Красноярский краеведческий музей. Наследники подарили дом городу. В 
                доме жила семья красноярского художника Дмитрия Иннокентьевича Каратанова, 
                который был первым преподавателем рисовальной школы. Во флигеле создавались и 
                работали различные общественные организации, например, Красноярское отделение 
                Союза художников СССР. Расселение дома Суриковых завершилось в конце 1940-х годов, 
                и 5 июня 1948 года музей открылся. Первое название музея — Дом-музей В. И. Сурикова. 
                В создании музея принимали участие дочери художника.
