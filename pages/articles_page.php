                Строительство началось весной 1914 года, после назначения на должность губернатора Енисейской губернии Ивана Ивановича Крафта.
                Из-за начала Первой мировой войны здание осталось недостроенным. В нём долго располагались казармы, а в 1920 году — военный госпиталь. 
                Здание музея было окончательно достроено только в 1929 году. С 1930 года экспозиция и фонды музея располагаются в 
                ныне существующем чернышёвском здании. Во время Великой Отечественной войны экспозиции были свёрнуты; 
                в здании размещалось Управление Северного морского пути. В 1987—2001 годах проводилась масштабная реконструкция музея. 
                С северной стороны построено современное фондохранилище. В 2013 году фасады музея отреставрировали по оригинальному проекту. 
                В октябре 1903 года Городской общественный музей был передан в ведение Красноярского подотдела Восточно-Сибирского 
                отдела Императорского Русского географического общества. В 1920 году музей передан Наркомату просвещения. 
                Название изменилось на «Государственный музей Приенисейского края». После образования Красноярского края в 1935 году музей 
                изменил название на Красноярский краеведческий музей.
